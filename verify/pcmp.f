      SUBROUTINE PCMP
      IMPLICIT NONE
 
C        PRINT MESSAGE DESCRIBING CRITERION OF COMPARISON
      include 'lcmp.inc'
C
      IF(LCMP.GE.0) GOTO 1110
      WRITE(6,1105)
 1105 FORMAT(10X,'STRICT EQUALITY')
      GOTO 1500
 1110 IF(LCMP.GT.0) GOTO 1120
      WRITE(6,1115)
 1115 FORMAT(10X,'COMPARE ONLY NON-BLANKS')
      GOTO 1500
 1120 IF(LCMP.GT.1) GOTO 1130
      WRITE(6,1125)
 1125 FORMAT(10X,'IGNORE FORTRAN LABELS')
      GOTO 1500
 1130 IF(LCMP.GT.2) GOTO 1140
      WRITE(6,1135)
 1135 FORMAT(10X,'ALLOW FORTRAN LABELS AT ANY COLUMN')
      GOTO 1500
 1140 IF(LCMP.GT.3) GOTO 1150
      WRITE(6,1145)
 1145 FORMAT(10X,'COMPARE NON-BLANKS, RECOGNIZE PEP')
      GOTO 1500
 1150 CALL PSCMP
 1500 RETURN
      END
