      SUBROUTINE PR2000(PREC)
C
C     CALCULATE PRECESSSION MATRIX FROM 1950.0 TO J2000
C     R.W.KING MAY 1982
C
      IMPLICIT NONE
C
      REAL*10 PREC(3,3),Z(6),CZ(3),SZ(3),TTRM(4),OCOF(4),ZCOF(3,3),
     1 CONVDS/4.84813681109535993591E-6_10/,E1,E2,E3,HXZ1,HXZ2,
     2 OBLQ0,OBLQ0X,Q1,Q2,R,T,TF
      INTEGER*4 I
C
      LOGICAL INIT/.FALSE./
      EQUIVALENCE (OCOF(1),E1),(OCOF(2),E2),(OCOF(3),E3)
C
C           USE NEW IAU(1976) EXPRESSIONS
C           (FROM LIESKE ET AL., ASTRON. ASTROPHYS. 58, 1-16, 1977)
      R= (2433282.42345905_10-2451545.0_10)
      TF= 0._10
C     TF IS JULIAN CENTURIES SINCE FUNDAMENTAL EPOCH (1950.0 OR J2000)
      T= R/36525._10
      TTRM(1)=CONVDS*T
      TTRM(2)=TTRM(1)*T
      TTRM(3)=TTRM(2)*T
C     T IS JULIAN CENTURIES SINCE FIXED EPOCH 1950.0
      IF(INIT) GOTO 5
      OBLQ0= 84381.448_10 - 46.8150_10*TF - 0.00059_10*TF**2
     1                            + 0.001813_10*TF**3
C     AT 1950.0 THIS GIVES OBLQ0= 84404.85522
      E1= -46.8150_10 - 0.00117_10*TF + 0.005439_10*TF**2
      E2= -0.00059_10 + 0.005439_10*TF
      E3=  0.001813_10
    5 OBLQ0X=OBLQ0
C
      HXZ1=0._10
      HXZ2=0._10
C
C       ZETA=Z(1),  Z=Z(2),  THETA=Z(3)
      IF(INIT) GOTO 6
      ZCOF(1,1)= 2306.2181_10 + HXZ1 + 1.39656_10*TF - 0.000139_10*TF**2
      ZCOF(1,2)= 0.30188_10 - 0.000345_10*TF
      ZCOF(1,3)= 0.017998_10
      ZCOF(2,1)=ZCOF(1,1)
      ZCOF(2,2)= 1.09468_10 + 0.000066_10*TF
      ZCOF(2,3)= 0.018203_10
      ZCOF(3,1)= 2004.3109_10 + HXZ2 - 0.85330_10*TF - 0.000217_10*TF**2
      ZCOF(3,2)=-0.42665_10 - 0.000217_10*TF
      ZCOF(3,3)=-0.041833_10
      INIT=.TRUE.
    6 CALL PRODCT(ZCOF,TTRM,Z,3,3,1)
      DO 13 I=1,3
      CZ(I)=COS(Z(I))
13    SZ(I)=SIN(Z(I))
C
C        PREC(J,K)= PRECESSION MATRIX  J,K=1,2,3
      Q1=CZ(2)*CZ(3)
      Q2=SZ(2)*CZ(3)
      PREC(1,1)=CZ(1)*Q1-SZ(1)*SZ(2)
      PREC(1,2)=-SZ(1)*Q1-CZ(1)*SZ(2)
      PREC(1,3)=-CZ(2)*SZ(3)
      PREC(2,1)=CZ(1)*Q2+SZ(1)*CZ(2)
      PREC(2,2)=CZ(1)*CZ(2)-SZ(1)*Q2
      PREC(2,3)=-SZ(2)*SZ(3)
      PREC(3,1)=CZ(1)*SZ(3)
      PREC(3,2)=-SZ(1)*SZ(3)
      PREC(3,3)=CZ(3)
C
   90 RETURN
      END
